#!/usr/bin/env node
/**
 * Stratified-and-even-spaced selection of n=100 study days from
 * candidate-days.csv. Marks `selected=Y` and `selection_bucket=<regime>`
 * on the selected rows; everything else stays blank.
 *
 *   node scripts/charm-pressure-capture/select-study-days.mjs
 *
 * Reads:  scripts/charm-pressure-capture/candidate-days.csv
 * Writes: scripts/charm-pressure-capture/candidate-days.csv (in place)
 * Writes: scripts/charm-pressure-capture/selected-days.md (human-readable list)
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const csvPath = join(__dirname, 'candidate-days.csv');
const mdPath = join(__dirname, 'selected-days.md');

// Stratification targets
const TARGETS = {
  range_bound: 50,
  trending: 30,
  event: 20,
};

// TRACE Charm Pressure renders historical data unreadably for sessions
// before this cutoff (the heatmap is too coarse to extract pin band /
// stability features cleanly). Skip them at selection time.
const MIN_DATE = '2024-12-09';

// ---------------------------------------------------------------------------
// Read CSV
// ---------------------------------------------------------------------------

const text = readFileSync(csvPath, 'utf8');
const lines = text.split('\n').filter((l) => l.length > 0);
const header = lines[0].split(',');
const dataLines = lines.slice(1).map((l) => l.split(','));

const idx = (col) => header.indexOf(col);
const dateIdx = idx('date');
const regimeIdx = idx('regime');
const selectedIdx = idx('selected');
const bucketIdx = idx('selection_bucket');
const halfDayIdx = idx('is_half_day');
const dowIdx = idx('day_of_week');

// Reset any prior selection
for (const r of dataLines) {
  r[selectedIdx] = '';
  r[bucketIdx] = '';
}

// ---------------------------------------------------------------------------
// Bucket eligible rows by regime, sorted by date.
// Eligibility = has a regime, is not a half-day.
// ---------------------------------------------------------------------------

const buckets = { range_bound: [], trending: [], event: [], mixed: [] };

for (let i = 0; i < dataLines.length; i += 1) {
  const r = dataLines[i];
  if (r[halfDayIdx] === '1') continue;
  if (r[dateIdx] < MIN_DATE) continue;
  const regime = r[regimeIdx];
  if (!regime || !(regime in buckets)) continue;
  buckets[regime].push(i);
}

// ---------------------------------------------------------------------------
// Even-spaced pick: N indices from M-length array, distributed across the span.
// Uses (M-1)/(N-1) step so the first and last rows are always included —
// this maximizes time coverage across the study window.
// ---------------------------------------------------------------------------

function evenSpacedPicks(arr, n) {
  if (n >= arr.length) return [...arr];
  if (n <= 0) return [];
  if (n === 1) return [arr[Math.floor(arr.length / 2)]];

  const step = (arr.length - 1) / (n - 1);
  const seen = new Set();
  const out = [];
  for (let i = 0; i < n; i += 1) {
    const idx = Math.round(i * step);
    if (!seen.has(idx)) {
      seen.add(idx);
      out.push(arr[idx]);
    }
  }
  return out;
}

// ---------------------------------------------------------------------------
// Apply selection
// ---------------------------------------------------------------------------

const picksByBucket = {};
for (const [bucket, target] of Object.entries(TARGETS)) {
  const indices = buckets[bucket];
  const picks = evenSpacedPicks(indices, target);
  picksByBucket[bucket] = picks;
  for (const i of picks) {
    dataLines[i][selectedIdx] = 'Y';
    dataLines[i][bucketIdx] = bucket;
  }
}

// ---------------------------------------------------------------------------
// Write CSV back
// ---------------------------------------------------------------------------

const out = [header.join(','), ...dataLines.map((r) => r.join(','))].join('\n');
writeFileSync(csvPath, `${out}\n`);

// ---------------------------------------------------------------------------
// Emit human-readable list
// ---------------------------------------------------------------------------

let md = '# Selected Study Days\n\n';
md += `Generated by \`select-study-days.mjs\`. Source: \`candidate-days.csv\`.\n\n`;
md += `Targets: range_bound=${TARGETS.range_bound}, trending=${TARGETS.trending}, event=${TARGETS.event}. `;
md += `Even-spaced across the study window (Jun 2024 → Apr 2026) within each bucket.\n\n`;

for (const bucket of ['range_bound', 'trending', 'event']) {
  const picks = picksByBucket[bucket];
  md += `## ${bucket} (n=${picks.length}/${TARGETS[bucket]} from ${buckets[bucket].length} candidates)\n\n`;
  md += '| # | Date | Day | Range % |\n|---|---|---|---|\n';
  for (let i = 0; i < picks.length; i += 1) {
    const r = dataLines[picks[i]];
    const date = r[dateIdx];
    const dow = r[dowIdx];
    const rngPct = r[idx('realized_range_pct')] || '—';
    md += `| ${i + 1} | ${date} | ${dow} | ${rngPct} |\n`;
  }
  md += '\n';
}

writeFileSync(mdPath, md);

// ---------------------------------------------------------------------------
// Console summary
// ---------------------------------------------------------------------------

console.log(
  'Selected study days written to candidate-days.csv (selected=Y) and selected-days.md',
);
console.log('');
for (const bucket of ['range_bound', 'trending', 'event']) {
  const got = picksByBucket[bucket].length;
  const have = buckets[bucket].length;
  console.log(
    `  ${bucket.padEnd(15)} ${got}/${TARGETS[bucket]} picked from ${have} candidates`,
  );
}
console.log('');
const total = Object.values(picksByBucket).reduce((s, a) => s + a.length, 0);
console.log(`  Total selected:  ${total}`);
